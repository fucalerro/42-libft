#include "libft.h"

int	ft_atoi(const char *nptr)
{
	size_t	*i;

	i = (size_t []){1, 0, 0};
	while (nptr[i[1]] == ' ')
		i[1]++;
	if (nptr[i[1]] == '+')
		i[1]++;
	if (nptr[i[1]] == '-')
	{
		i[0] = -1;
		i[1]++;
	}
	while (nptr[i[1]] >= '0' && nptr[i[1]] <= '9')
		i[2] = i[2] * 10 + nptr[i[1]++] - 48;
	return (i[2] * i[0]);
}
