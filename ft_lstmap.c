#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_lst;
	t_list	*new_node;
	void	*new_content;

	while (lst)
	{
		new_content = f(lst->content);
		new_node = ft_lstnew(new_content);
		if (new_node == NULL)
		{
			return (NULL);
		}
		ft_lstadd_back(&new_lst, new_node);
		if (del)
		{
			del(lst->content);
		}
		lst = lst->next;
	}
	return (new_lst);
}
